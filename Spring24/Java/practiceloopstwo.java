import java.util.Scanner;

public class practiceloopstwo {
	
	public static void main(String[] args) {
		
		for(int i = 8; i < 13; i++) {
			System.out.println(i);
		}

		int j = 10;
		while(j != 0) {
			System.out.println(j);
			j--;
		}

		for(int x = 0; x < 102; x += 2) {
			System.out.println(x);
		}

	}
}
